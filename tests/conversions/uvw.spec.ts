import { UVWtoRGB } from "../../src/conversions/uvw";

describe("UVWtoRGB", () => {
  test.each`
    name         | input                                                                                 | expected
    ${"Black"}   | ${{ u: 0, v: 0, w: 0, a: 1 }}                                                         | ${[0, 0, 0, 1]}
    ${"White"}   | ${{ u: 0.6428637773874961, v: 1.0000000361162846, w: 1.4304044954174074, a: 1 }}      | ${[255, 255, 255, 1]}
    ${"Red"}     | ${{ u: 0.29071049521654068, v: 0.2224931917562371, w: 0.12266886847242185, a: 1 }}    | ${[255, 0, 0, 1]}
    ${"Lime"}    | ${{ u: 0.2567676458891941, v: 0.7168870538238823, w: 0.9312954891518012, a: 1 }}      | ${[0, 255, 0, 1]}
    ${"Blue"}    | ${{ u: 0.0953856362817613, v: 0.0606197905361654, w: 0.37644013779318485, a: 1 }}     | ${[0, 0, 255, 1]}
    ${"Yellow"}  | ${{ u: 0.5474781411057348, v: 0.9393802455801193, w: 1.0539643576242228, a: 1 }}      | ${[255, 255, 0, 1]}
    ${"Cyan"}    | ${{ u: 0.35215328217095546, v: 0.7775068443600475, w: 1.3077356269449856, a: 1 }}     | ${[0, 255, 255, 1]}
    ${"Magenta"} | ${{ u: 0.386096131498302, v: 0.28311298229240258, w: 0.49910900626560684, a: 1 }}     | ${[255, 0, 255, 1]}
    ${"Silver"}  | ${{ u: 0.3388632208293239, v: 0.5271151447432529, w: 0.7539878454121067, a: 1 }}      | ${[192, 192, 192, 1]}
    ${"Gray"}    | ${{ u: 0.1387688964919753, v: 0.21586050790997852, w: 0.30876782974597134, a: 1 }}    | ${[128, 128, 128, 1]}
    ${"Maroon"}  | ${{ u: 0.06275291288580178, v: 0.04802749164443902, w: 0.026479363296863108, a: 1 }}  | ${[128, 0, 0, 1]}
    ${"Olive"}   | ${{ u: 0.11817890534051182, v: 0.20277508960804203, w: 0.22750927333898925, a: 1 }}   | ${[128, 128, 0, 1]}
    ${"Green"}   | ${{ u: 0.055425992454710034, v: 0.15474759796360301, w: 0.20102991004212615, a: 1 }}  | ${[0, 128, 0, 1]}
    ${"Purple"}  | ${{ u: 0.08334290403726525, v: 0.0611129099463755, w: 0.10773791970384513, a: 1 }}    | ${[128, 0, 128, 1]}
    ${"Teal"}    | ${{ u: 0.07601598360617354, v: 0.16783301626553948, w: 0.28228846644910817, a: 1 }}   | ${[0, 128, 128, 1]}
    ${"Navy"}    | ${{ u: 0.020589991151463485, v: 0.013085418301936479, w: 0.08125855640698204, a: 1 }} | ${[0, 0, 128, 1]}
  `("$name", ({ input, expected }) => {
    expect(Object.values(UVWtoRGB(input)).map((val) => Math.abs(Math.round(val)))).toEqual(expected);
  });
});

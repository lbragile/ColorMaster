import { CMYKtoRGB } from "../../src/conversions/cmyk";

describe("CMYKtoRGB", () => {
  test.each`
    name         | input                                                   | expected
    ${"Black"}   | ${{ c: 0, m: 0, y: 0, k: 100, a: 1 }}                   | ${{ r: 0, g: 0, b: 0, a: 1 }}
    ${"White"}   | ${{ c: 0, m: 0, y: 0, k: 0, a: 1 }}                     | ${{ r: 255, g: 255, b: 255, a: 1 }}
    ${"Red"}     | ${{ c: 0, m: 100, y: 100, k: 0, a: 1 }}                 | ${{ r: 255, g: 0, b: 0, a: 1 }}
    ${"Lime"}    | ${{ c: 100, m: 0, y: 100, k: 0, a: 1 }}                 | ${{ r: 0, g: 255, b: 0, a: 1 }}
    ${"Blue"}    | ${{ c: 100, m: 100, y: 0, k: 0, a: 1 }}                 | ${{ r: 0, g: 0, b: 255, a: 1 }}
    ${"Yellow"}  | ${{ c: 0, m: 0, y: 100, k: 0, a: 1 }}                   | ${{ r: 255, g: 255, b: 0, a: 1 }}
    ${"Cyan"}    | ${{ c: 100, m: 0, y: 0, k: 0, a: 1 }}                   | ${{ r: 0, g: 255, b: 255, a: 1 }}
    ${"Magenta"} | ${{ c: 0, m: 100, y: 0, k: 0, a: 1 }}                   | ${{ r: 255, g: 0, b: 255, a: 1 }}
    ${"Silver"}  | ${{ c: 0, m: 0, y: 0, k: 24.705882352941178, a: 1 }}    | ${{ r: 192, g: 192, b: 192, a: 1 }}
    ${"Gray"}    | ${{ c: 0, m: 0, y: 0, k: 49.80392156862745, a: 1 }}     | ${{ r: 128, g: 128, b: 128, a: 1 }}
    ${"Maroon"}  | ${{ c: 0, m: 100, y: 100, k: 49.80392156862745, a: 1 }} | ${{ r: 128, g: 0, b: 0, a: 1 }}
    ${"Olive"}   | ${{ c: 0, m: 0, y: 100, k: 49.80392156862745, a: 1 }}   | ${{ r: 128, g: 128, b: 0, a: 1 }}
    ${"Green"}   | ${{ c: 100, m: 0, y: 100, k: 49.80392156862745, a: 1 }} | ${{ r: 0, g: 128, b: 0, a: 1 }}
    ${"Purple"}  | ${{ c: 0, m: 100, y: 0, k: 49.80392156862745, a: 1 }}   | ${{ r: 128, g: 0, b: 128, a: 1 }}
    ${"Teal"}    | ${{ c: 100, m: 0, y: 0, k: 49.80392156862745, a: 1 }}   | ${{ r: 0, g: 128, b: 128, a: 1 }}
    ${"Navy"}    | ${{ c: 100, m: 100, y: 0, k: 49.80392156862745, a: 1 }} | ${{ r: 0, g: 0, b: 128, a: 1 }}
  `("$name", ({ input, expected }) => {
    expect(CMYKtoRGB(input)).toMatchObject(expected);
  });
});
